CREATE TABLE WATCH_HISTORY (
    WATCH_ID NUMBER PRIMARY KEY,
    USER_ID NUMBER,
    COUNTRY_ID NUMBER,
    MOVIE_NAME VARCHAR2(100),
    GENRE VARCHAR2(50),
    WATCH_DATE DATE,
    WATCH_DURATION NUMBER,

    CONSTRAINT FK_WATCH_USER
    FOREIGN KEY (USER_ID)
    REFERENCES USERS(USER_ID),

    CONSTRAINT FK_WATCH_COUNTRY
