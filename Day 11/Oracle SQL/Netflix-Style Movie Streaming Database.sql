CREATE TABLE USERS (
    USER_ID NUMBER PRIMARY KEY,
    USER_NAME VARCHAR2(50) NOT NULL,
    EMAIL VARCHAR2(100) UNIQUE,
    COUNTRY_ID NUMBER,
    SUBSCRIPTION_TYPE VARCHAR2(20),

    CONSTRAINT FK_USER_COUNTRY
    FOREIGN KEY (COUNTRY_ID)
    REFERENCES COUNTRIES(COUNTRY_ID)
);

